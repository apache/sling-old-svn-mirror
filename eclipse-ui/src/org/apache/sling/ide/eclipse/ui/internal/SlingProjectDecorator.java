package org.apache.sling.ide.eclipse.ui.internal;

import org.apache.sling.ide.eclipse.core.internal.ProjectHelper;
import org.eclipse.core.resources.IProject;
import org.eclipse.jface.viewers.IDecoration;
import org.eclipse.jface.viewers.ILabelProviderListener;
import org.eclipse.jface.viewers.ILightweightLabelDecorator;

public class SlingProjectDecorator implements ILightweightLabelDecorator {

	@Override
	public void addListener(ILabelProviderListener listener) {
	}

	@Override
	public void dispose() {
	}

	@Override
	public boolean isLabelProperty(Object element, String property) {
		return false;
	}

	@Override
	public void removeListener(ILabelProviderListener listener) {
	}

	@Override
	public void decorate(Object element, IDecoration decoration) {
		if (!(element instanceof IProject)) {
			return;
		}
		IProject project = (IProject)element;
		if (ProjectHelper.isBundleProject(project) || ProjectHelper.isContentProject(project)) {
			// then decorate
			decoration.addOverlay(SharedImages.SLING_DECORATION, IDecoration.BOTTOM_RIGHT);
		} else {
			// dont decorate
			return;
		}
	}


}
